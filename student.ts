export class Student {
  sId?: any;
  public sName: any;
  public sEmail?: any;
  public sAddress?: any;
  public sPhone?: any;
  public sAge?: any;
  public sClass?: any;
  constructor(
    sId?: any,
    sName?: any,
    sEmail?: any,
    sAddress?: any,
    sPhone?: any,
    sAge?: any,
    sClass?: any
  ) {
    this.sId = sId;
    this.sName = sName;
    this.sEmail = sEmail;
    this.sAddress = sAddress;
    this.sPhone = sPhone;
    this.sAge = sAge;
    this.sClass = sClass;
    
  }
}
